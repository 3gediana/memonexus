def main():
    event_bus = AgentEventBus()

    total = len(SEED_MESSAGES)
    stored_count = 0
    skipped_count = 0
    failed_count = 0
    all_stored = []  # (fingerprint, simulated_date) pairs

    print()
    print("=" * 64)
    print("  [SEED] Study Mentor Memory Seeder")
    print(f"  {total} messages | Full storage pipeline")
    print("  RoutingAgent -> KeyAgent -> AssociationAgent")
    print("=" * 64)
    print()

    t0 = time.time()

    for i, (sim_date, msg) in enumerate(SEED_MESSAGES):
        idx = f"[{i+1:3d}/{total}]"
        short = msg[:45] + ("..." if len(msg) > 45 else "")
        print(f"{idx} {sim_date} | {short}")

        try:
            t1 = time.time()
            result = process_user_message(msg, turn_index=i + 1, event_bus=event_bus)
            elapsed = time.time() - t1

            if result.get("success"):
                added = result.get("memories_added", [])
                if added:
                    stored_count += len(added)
                    for m in added:
                        fp = m.get("fingerprint")
                        if fp:
                            all_stored.append((fp, sim_date))
                    keys_hit = ", ".join(m.get("key", "?") for m in added)
                    print(f"      [OK] +{len(added)} -> [{keys_hit}] ({elapsed:.1f}s)")
                else:
                    skipped_count += 1
                    print(f"      [--] no memory ({elapsed:.1f}s)")
            else:
                failed_count += 1
                print(f"      [FAIL] {result.get('error', 'unknown')} ({elapsed:.1f}s)")
        except Exception as e:
            failed_count += 1
            print(f"      [ERR] {e}")

        # 简单限速，避免 API 过载
        time.sleep(0.3)

    # ── 批量更新时间戳 ──
    print()
    print("[TIME] Updating timestamps...")
    updated = _batch_update_timestamps(all_stored)
    print(f"  Updated {updated} memories")

    total_time = time.time() - t0
    print()
    print("=" * 64)
    print(f"  DONE!")
    print(f"     Stored:  {stored_count} memories")
    print(f"     Skipped: {skipped_count}")
    print(f"     Failed:  {failed_count}")
    print(f"     Time:    {total_time / 60:.1f} min")
    print("=" * 64)
    print()

    # ── 验证统计 ──
    _print_summary()


def _batch_update_timestamps(stored_items: list) -> int:
    """批量更新已存储记忆的 created_at 时间戳，模拟不同日期"""
    if not stored_items:
        return 0

    conn = get_db()
    count = 0
    try:
        for fp, sim_date in stored_items:
            # 生成一个随机的时间点（8:00-22:00）
            hour = random.randint(8, 22)
            minute = random.randint(0, 59)
            second = random.randint(0, 59)
            ts = f"{sim_date}T{hour:02d}:{minute:02d}:{second:02d}+08:00"
            conn.execute(
                "UPDATE memory SET created_at = ? WHERE fingerprint = ?",
                (ts, fp),
            )
            count += 1
        conn.commit()
    finally:
        conn.close()

    return count


def _print_summary():
    """打印当前记忆库统计"""
    conn = get_db()
    try:
        total = conn.execute("SELECT COUNT(*) as c FROM memory").fetchone()["c"]
        by_key = conn.execute(
            "SELECT key, COUNT(*) as c FROM memory GROUP BY key ORDER BY c DESC"
        ).fetchall()
        edge_count = conn.execute("SELECT COUNT(*) as c FROM edges").fetchone()["c"]
    finally:
        conn.close()

    print()
    print("[STATS] Memory DB status:")
    print(f"   Total memories: {total}")
    print(f"   Total edges:    {edge_count}")
    print(f"   By key:")
    for row in by_key:
        bar = "#" * min(row["c"], 40)
        print(f"     {row['key']:15s} | {bar} {row['c']}")
    print()


if __name__ == "__main__":
    main()
